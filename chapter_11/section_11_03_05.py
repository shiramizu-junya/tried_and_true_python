import datetime

dt_1 = datetime.date(2021, 2, 10)
print(dt_1)  # => 2021-02-10

dt_2 = datetime.time(15, 30, 45)
print(dt_2)  # => 15:30:45

dt_3 = datetime.datetime(2021, 2, 10, 15, 30, 45)
print(dt_3)  # => 2021-02-10 15:30:45

dt_4 = datetime.datetime(2021, 3, 10)
print(dt_4)  # => 2021-03-10 00:00:00

